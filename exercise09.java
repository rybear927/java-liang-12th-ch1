//(Area and perimeter of a rectangle) Write a program that displays the area and
//perimeter of a rectangle with a width of 5.3 and height of 8.6 using the following
//formula:
//area = width * height
//perimeter = 2 * (width + height)

public class exercise09 {
    public static void main(String[] args) {
        System.out.println("Rectangle width: 5.3");
        System.out.println("Rectangle height: 8.6");
        System.out.println("Area: " + 5.3 * 8.6);
        System.out.println("Perimeter: " + 2 * (5.3 * 8.6));
    }
}
